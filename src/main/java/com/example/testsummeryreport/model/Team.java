package com.example.testsummeryreport.model;

public class Team {

    private String teamName;

    private String qeLeadName;

    private String qeManagerName;

    public Team() {
    }

    public String getTeamName() {
        return teamName;
    }

    public void setTeamName(String teamName) {
        this.teamName = teamName;
    }

    public String getQeLeadName() {
        return qeLeadName;
    }

    public void setQeLeadName(String qeLeadName) {
        this.qeLeadName = qeLeadName;
    }

    public String getQeManagerName() {
        return qeManagerName;
    }

    public void setQeManagerName(String qeManagerName) {
        this.qeManagerName = qeManagerName;
    }

}
